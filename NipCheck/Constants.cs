        public static Error CheckLine(string line, int ln)
        {
            Error result = new Error();

            if (line.Length < 3 || line.StartsWith(@"//") || line.StartsWith(@"/*") || line.StartsWith(@"*") || line.Contains(@"*/")) 
            {
                return null;
            }

            if (line.Contains(@"//"))
            {
                int index = line.IndexOf(@"//");
                line = line.Substring(0, index); 
            }

            result.Query = line;

            bool hasError = false;
            string error = "";
            MatchCollection mc = Regex.Matches(line, @"[^\W\d](\w|[-']{1,2}(?=\w))*", 0);

            foreach (Match m in mc)
            {
                if (!defined.ContainsKey(m.Value.ToLower()))
                {
                    error += m.Value + " ";
                    hasError = true;
                }
            }

            if (hasError)
            {
                result.Identifiers = "Unknown Identifiers: " + error + " ";
            }

            int bracket0 = Regex.Matches(line, @"\[").Count;
            int bracket1 = Regex.Matches(line, @"\]").Count;

            if (bracket0 != bracket1)
            {
                result.Syntax = "Syntax Errors: Unmatched brackets\n";
                result.Line = ln + 1;
                return result;
            }

            MSScriptControl.ScriptControl sc = new MSScriptControl.ScriptControl();
            sc.Language = "JScript";

            string[] expression = Regex.Replace(line, @"[^\W\d](\w|[-']{1,2}(?=\w))*|\[.*?\]", "0").Replace("-", "").Split('#');
            for (int i = 0; i < expression.Length; i++)
            {
                if (expression[i].Length < 3)
                    continue;

                try
                {
                    sc.Eval(expression[i]);
                }
                catch
                {
                    result.Syntax = "Syntax Errors: failed to evaluate \n";
                    hasError = true;
                }
            }

            if (hasError)
            {
                result.Line = ln + 1;
                return result;
            }

            return null;
        }
